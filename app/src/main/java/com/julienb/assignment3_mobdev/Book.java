package com.julienb.assignment3_mobdev;

// just a simple MenuItem class with a couple attributes
public class Book {

    public String name, description, author, price, image;

    public Book(String name,String author, String description, String price, String image){
        this.name = name;
        this.description = description;
        this.author = author;
        this.price = price;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
